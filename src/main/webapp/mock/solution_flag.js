function getFlagName(index){
    switch(index){
            case 0:
            return "未受理";
            break;
            case 1:
            return "正在处理";
            break;
            case 2:
            return "等待确认";
            break;
            case 3:
            return "已反馈";
            break;
            case 4:
            return "暂存";
            break;
            case 5:
            return "请送修";
            break;
            case 6:
            return "更换硬件";
            break;
            case 7:
            return "经费开支";
            break;
            case 8:
            return "建议报废";
            break;
            case 9:
            return "流程结束";
            break;
    }
    
}